#include <iostream>

int main()
{
    auto x = 5; // int x = 5
    auto y = 3.14; // double x = 3.14
    auto y1 = 3.14f; // float x = 3.14f
    auto z = 'a'; // char z = 'a'
    std::cout << "x: " << x << '\n';
    std::cout << "y: " << y << '\n';
    std::cout << "y: " << y1 << '\n';
    std::cout << "z: " << z << '\n';
    
    return 0;
}
